function changebg(i){

    switch(i){
        case 1: document.body.style.backgroundColor = "#FE0202";
            break;
        case 2: document.body.style.backgroundColor = "#59BC98";
            break;
        case 3: document.body.style.backgroundColor = "#2594D2";
            break;
        case 4: document.body.style.backgroundColor = "#72BC59";
            break;
        case 5: document.body.style.backgroundColor = "#BC59A0";
            break;
        case 6: document.body.style.backgroundColor = "#DCF73C";
            break;
    }


}